function predictThreeCards() {
    if (typeof jatakas === 'undefined') return;

    let selections = [];
    while (selections.length < 3) {
        let rand = jatakas[Math.floor(Math.random() * jatakas.length)];
        if (!selections.includes(rand)) {
            selections.push(rand);
        }
    }

document.getElementById("result").innerHTML = `
    <div style="display: flex; justify-content: space-between; gap: 10px; margin-top: 20px;">
        ${selections.map((item, index) => `
            <div style="flex: 1; padding: 10px; border: 3px solid #b8860b; border-radius: 10px; background: #fffaf0;">
                <h4 style="margin: 5px 0;">${['เหตุ จากอดีต', 'สภาวะ ปัจจุบัน', 'โพธิธรรม สู่อนาคต'][index]}</h4>
                <p style="font-size: 14px; margin: 5px 0;"><b>${item.name}</b></p>
                <p style="font-size: 12px; margin: 5px 0;">${item.moral}</p>
            </div>
        `).join('')}
    </div>
`;
}
